#B) Sprawdź i wyjaśnij działanie następujących instrukcji:

#1. int(3.0)
print(int(3.0))
#Funkcja int() konwertuje (zamienia) wartość na liczbę całkowitą.

#2. float(3)
print(float(3))
#Funkcja float() konwertuje ją na liczbę zmiennoprzecinkową (czyli z kropką).

#3. float("3")
print(int("3"))
#Cudzysłów zamienia wartość w tekst, int() i float() potrafią konwertować tylko takie teksty, które wyglądają jak liczby.

#4. str(12.4)
print(str(12.4))
#Funkcja str() konwetuje int lub float na lanuch znakow (string)

#5. bool(0)
print(bool(0))
#Funkcja bool(0) to logiczny typ danych, który w przypadku fałszu zwraca 0, a jeśli prawda zwraca 1.