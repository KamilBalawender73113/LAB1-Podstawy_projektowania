'''Napisz skrypt, który pobiera długości boków prostokąta, a następnie oblicza jego pole i
obwód oraz wyświetla wyniki na ekranie.
'''
bok1=float(input("Podaj długość pierwszego boku:"))
bok2=float(input("Podaj długość drugiego boku:"))
pole_prostokąta=(bok1*bok2)
obwód_prostokąta=(2*bok1)+(2*bok2)
print("Pole prostokąta wynosi",pole_prostokąta,"\nObwód prostokąta wynosi:",obwód_prostokąta)
