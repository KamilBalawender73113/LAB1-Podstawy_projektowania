#A) Sprawdź w interpreterze typ wyników określonych działań - type( x ) i wyjaśnij, co oznaczają poszczególne operatory?

#1. 1 + 2
a = 1+2
print(a, type(a)) # "+" oznacza sumę.

#2. 1 + 4.5
b = 1+4.5
print(b, type(b)) # "+" oznacza sumę.

#3. 3 / 2
c = 3/2
print(c, type(c)) # "/" oznacza dzielenie.

#4. 4 / 2
d = 4/2
print(d, type(d)) # "/" oznacza dzielenie.

#5. 3 // 2
e = 3//2
print(e, type(e)) # "//" oznacza dzielenie bez reszty, wyciąga jedynie całości.

#6. -3 // 2
f= -3//2
print(f, type(f)) # "//" oznacza dzielenie bez reszty, wyciąga jedynie całości.

#7. 11 % 2
g= 11%2
print(g, type(g)) # "%" oznacza zwracanie tego, co zostaje po podzieleniu jednej liczby przez drugą.

#8. 2 ** 10
h= 2**10
print(h, type(h)) # "**" oznacza podnoszenie do potęgi

#9. 8 ** (1/3)
i= 8**(1/3)
print(i, type(i)) # "**" oznacza podnoszenie do potęgi

#int - Przechowuje liczby całkowite, bez części ułamkowej.
#float - Przechowuje liczby rzeczywiste, czyli z częścią ułamkową.



